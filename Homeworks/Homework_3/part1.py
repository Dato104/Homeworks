

age = input("მიუთითეთ თქვენი ასაკი: ")
age = int(age)

if 1 <= age <= 12:
    print("თქვენ ხართ: ბავშვი")
elif 13 <= age <= 19:
    print("თქვენ ხართ: თინეიჯერი")
elif 20 <= age <= 64:
    print("თქვენ ხართ: ზრდასრული")
elif age >= 65:
    print("თქვენ ხართ: უფროსი")
else:
    print("შეცდომა!!!")


























































































